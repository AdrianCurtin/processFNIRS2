function html = tableToHTML(T)
    varNames = T.Properties.VariableNames;
    useRowNames = ~isempty(T.Properties.RowNames);

    html = '<table>';

    % Header
    html = [html, '<tr>'];
    if useRowNames
        html = [html, '<th></th>'];
    end
    for c = 1:width(T)
        html = [html, sprintf('<th>%s</th>', varNames{c})]; %#ok<AGROW>
    end
    html = [html, '</tr>'];

    % Rows
    for r = 1:height(T)
        html = [html, '<tr>']; %#ok<AGROW>
        if useRowNames
            html = [html, sprintf('<td><strong>%s</strong></td>', ...
                T.Properties.RowNames{r})]; %#ok<AGROW>
        end
        for c = 1:width(T)
            val = T.(varNames{c})(r);
            if isnumeric(val)
                if isnan(val)
                    cellStr = '-';
                else
                    cellStr = sprintf('%.3f', val);
                end
            elseif isstring(val) || ischar(val)
                cellStr = char(val);
            elseif iscell(val)
                cellStr = char(string(val{1}));
            else
                cellStr = char(string(val));
            end
            html = [html, sprintf('<td>%s</td>', cellStr)]; %#ok<AGROW>
        end
        html = [html, '</tr>']; %#ok<AGROW>
    end

    html = [html, '</table>'];
end
