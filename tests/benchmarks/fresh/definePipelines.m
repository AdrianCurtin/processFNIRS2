function pipelines = definePipelines()
% DEFINEPIPELINES Define FRESH benchmark pipeline configurations
%
% Returns a struct array of pipeline configurations covering key
% preprocessing combinations from the FRESH study (Yuecel et al. 2025).
% Each pipeline specifies QC channel rejection, motion correction,
% filtering, short-channel regression, analysis method, and biomarker.
%
% Syntax:
%   pipelines = definePipelines()
%
% Outputs:
%   pipelines - Struct array with fields:
%     .name          - Pipeline identifier [char]
%     .description   - Human-readable description [char]
%     .rawMethods    - Cell array of raw-stage function specs
%     .oxyMethods    - Cell array of oxy-stage function specs
%     .useSSR        - Apply short-channel regression [logical]
%     .ssrMethod     - SSR method: 'nearest', 'pca', 'all' [char]
%     .analysisType  - 'blockavg' or 'glm' [char]
%     .glmMethod     - 'OLS' or 'AR-IRLS' (if glm) [char]
%     .dpfMode       - 'None', 'Fixed', or 'Calc' [char]
%     .baselineWindow - [start, end] relative to block onset [1x2]
%     .epochWindow   - [pre, post] seconds around block [1x2]
%     .glmDriftOrder - Polynomial drift order for GLM [scalar]
%     .useQC         - Run QC pipeline before processing [logical]
%     .qcChecks      - Cell array of QC check names to apply
%     .qcParams      - Struct of QC parameter overrides
%     .biomarker     - Biomarker field name for analysis (default: 'HbO')
%
% Reference:
%   Yuecel, M. A. et al. (2025). FRESH. Communications Biology.
%
% See also: benchmarks.fresh.runDatasetII, benchmarks.fresh.runDatasetI

% Default epoch/baseline settings
defBL = [-5, 0];
defEpoch = [5, 5];  % 5s pre, 5s post around block
defDPF = 'Fixed';  % Fixed DPF=5.93 (van der Zee 1992); hypothesis tests are relative so exact value doesn't affect conclusions
defDrift = 3;
defBio = 'HbO';

% Default QC: off
defQC = false;
defQCChecks = {};
defQCParams = struct();

% Accumulate as cell array, convert at end
pipeList = {};

% =====================================================================
%  SECTION 1: Pipelines WITHOUT QC (original set)
% =====================================================================

% --- Pipeline 1: Minimal (baseline subtraction only) ---
pipeList{end+1} = makePipeline( ...
    'minimal', ...
    'No preprocessing, baseline subtraction only', ...
    {}, {}, ...
    false, '', 'blockavg', '', defDPF, defBL, defEpoch, defDrift, ...
    defQC, defQCChecks, defQCParams, defBio);

% --- Pipeline 2: TDDR only ---
pipeList{end+1} = makePipeline( ...
    'tddr_only', ...
    'TDDR motion correction, no filtering', ...
    {{'pf2_Intensity2OD', {}}, {'pf2_MotionCorrectTDDR', {}}}, {}, ...
    false, '', 'blockavg', '', defDPF, defBL, defEpoch, defDrift, ...
    defQC, defQCChecks, defQCParams, defBio);

% --- Pipeline 3: TDDR + BPF (0.01-0.5 Hz) ---
pipeList{end+1} = makePipeline( ...
    'tddr_bpf', ...
    'TDDR + bandpass filter (0.01-0.5 Hz)', ...
    {{'pf2_Intensity2OD', {}}, {'pf2_MotionCorrectTDDR', {}}, {'bpf', {}}}, {}, ...
    false, '', 'blockavg', '', defDPF, defBL, defEpoch, defDrift, ...
    defQC, defQCChecks, defQCParams, defBio);

% --- Pipeline 4: sSMART + LPF (0.1 Hz) ---
pipeList{end+1} = makePipeline( ...
    'ssmart_lpf', ...
    'sSMART motion artifact rejection + lowpass 0.1 Hz', ...
    {{'pf2_Intensity2OD', {}}, {'pf2_sSMART', {}}}, ...
    {{'lpf', {}}}, ...
    false, '', 'blockavg', '', defDPF, defBL, defEpoch, defDrift, ...
    defQC, defQCChecks, defQCParams, defBio);

% --- Pipeline 5: Wavelet + BPF ---
pipeList{end+1} = makePipeline( ...
    'wavelet_bpf', ...
    'Wavelet motion correction + bandpass filter', ...
    {{'pf2_Intensity2OD', {}}, {'pf2_MotionCorrectWavelet', {}}, {'bpf', {}}}, {}, ...
    false, '', 'blockavg', '', defDPF, defBL, defEpoch, defDrift, ...
    defQC, defQCChecks, defQCParams, defBio);

% --- Pipeline 6: Spline + BPF ---
pipeList{end+1} = makePipeline( ...
    'spline_bpf', ...
    'Spline motion correction + bandpass filter (0.01-0.5 Hz)', ...
    {{'pf2_Intensity2OD', {}}, {'pf2_MotionCorrectSpline', {}}, {'bpf', {}}}, {}, ...
    false, '', 'blockavg', '', defDPF, defBL, defEpoch, defDrift, ...
    defQC, defQCChecks, defQCParams, defBio);

% --- Pipeline 7: Spline + Wavelet (hybrid) ---
pipeList{end+1} = makePipeline( ...
    'spline_wavelet', ...
    'Spline then wavelet motion correction (hybrid)', ...
    {{'pf2_Intensity2OD', {}}, {'pf2_MotionCorrectSpline', {}}, {'pf2_MotionCorrectWavelet', {}}}, {}, ...
    false, '', 'blockavg', '', defDPF, defBL, defEpoch, defDrift, ...
    defQC, defQCChecks, defQCParams, defBio);

% --- Pipeline 8: Spline + SSR + GLM (OLS) ---
pipeList{end+1} = makePipeline( ...
    'spline_ssr_glm', ...
    'Spline + short-channel regression + GLM (OLS)', ...
    {{'pf2_Intensity2OD', {}}, {'pf2_MotionCorrectSpline', {}}}, {}, ...
    true, 'nearest', 'glm', 'OLS', defDPF, defBL, defEpoch, defDrift, ...
    defQC, defQCChecks, defQCParams, defBio);

% --- Pipeline 9: TDDR + SSR + block averaging ---
pipeList{end+1} = makePipeline( ...
    'tddr_ssr_blockavg', ...
    'TDDR + short-channel regression + block averaging', ...
    {{'pf2_Intensity2OD', {}}, {'pf2_MotionCorrectTDDR', {}}}, {}, ...
    true, 'nearest', 'blockavg', '', defDPF, defBL, defEpoch, defDrift, ...
    defQC, defQCChecks, defQCParams, defBio);

% --- Pipeline 10: TDDR + GLM (OLS) ---
pipeList{end+1} = makePipeline( ...
    'tddr_glm_ols', ...
    'TDDR + GLM with ordinary least squares', ...
    {{'pf2_Intensity2OD', {}}, {'pf2_MotionCorrectTDDR', {}}}, {}, ...
    false, '', 'glm', 'OLS', defDPF, defBL, defEpoch, defDrift, ...
    defQC, defQCChecks, defQCParams, defBio);

% --- Pipeline 11: TDDR + SSR + GLM (OLS) ---
pipeList{end+1} = makePipeline( ...
    'tddr_ssr_glm', ...
    'TDDR + short-channel regression + GLM (OLS)', ...
    {{'pf2_Intensity2OD', {}}, {'pf2_MotionCorrectTDDR', {}}}, {}, ...
    true, 'nearest', 'glm', 'OLS', defDPF, defBL, defEpoch, defDrift, ...
    defQC, defQCChecks, defQCParams, defBio);

% --- Pipeline 12: TDDR + SSR + GLM (AR-IRLS) ---
pipeList{end+1} = makePipeline( ...
    'tddr_ssr_glm_arirls', ...
    'TDDR + SSR + GLM with AR-IRLS (full pipeline)', ...
    {{'pf2_Intensity2OD', {}}, {'pf2_MotionCorrectTDDR', {}}}, {}, ...
    true, 'nearest', 'glm', 'AR-IRLS', defDPF, defBL, defEpoch, defDrift, ...
    defQC, defQCChecks, defQCParams, defBio);

% --- Pipeline 13: Takizawa rejection + BPF ---
pipeList{end+1} = makePipeline( ...
    'takizawa_bpf', ...
    'Takizawa channel rejection + bandpass filter', ...
    {}, ...
    {{'pf2_TakizawaRejection', {}}, {'bpf', {}}}, ...
    false, '', 'blockavg', '', defDPF, defBL, defEpoch, defDrift, ...
    defQC, defQCChecks, defQCParams, defBio);

% --- Pipeline 14: SplineSG + BPF (matches FRESH groups 17, 38) ---
pipeList{end+1} = makePipeline( ...
    'splineSG_bpf', ...
    'Savitzky-Golay motion correction + bandpass filter (0.01-0.5 Hz)', ...
    {{'pf2_Intensity2OD', {}}, {'pf2_MotionCorrectSplineSG', {}}, {'bpf', {}}}, {}, ...
    false, '', 'blockavg', '', defDPF, defBL, defEpoch, defDrift, ...
    defQC, defQCChecks, defQCParams, defBio);

% --- Pipeline 15: CBSI analysis signal (matches FRESH groups 15, 34) ---
pipeList{end+1} = makePipeline( ...
    'cbsi_tddr_bpf', ...
    'TDDR + BPF with CBSI as analysis signal', ...
    {{'pf2_Intensity2OD', {}}, {'pf2_MotionCorrectTDDR', {}}, {'bpf', {}}}, {}, ...
    false, '', 'blockavg', '', defDPF, defBL, defEpoch, defDrift, ...
    defQC, defQCChecks, defQCParams, 'CBSI');

% =====================================================================
%  SECTION 2: Pipelines WITH QC pre-processing
%  These mirror common group approaches from the FRESH paper.
% =====================================================================

% --- QC-A: SCI-only channel rejection (Pollonini et al. 2014 style) ---
%     Reject channels with poor optode-scalp coupling (SCI < 0.75)
sciParams = struct('SCIThreshold', 0.75);
pipeList{end+1} = makePipeline( ...
    'qc_sci_tddr_bpf', ...
    'SCI channel rejection + TDDR + BPF', ...
    {{'pf2_Intensity2OD', {}}, {'pf2_MotionCorrectTDDR', {}}, {'bpf', {}}}, {}, ...
    false, '', 'blockavg', '', defDPF, defBL, defEpoch, defDrift, ...
    true, {'sci'}, sciParams, defBio);

% --- QC-B: SCI + Cardiac (strict coupling check) ---
%     Require both good SCI and detectable heartbeat
sciCardiacParams = struct('SCIThreshold', 0.75, 'CardiacSNR', 3);
pipeList{end+1} = makePipeline( ...
    'qc_sci_cardiac_tddr_bpf', ...
    'SCI + cardiac QC + TDDR + BPF', ...
    {{'pf2_Intensity2OD', {}}, {'pf2_MotionCorrectTDDR', {}}, {'bpf', {}}}, {}, ...
    false, '', 'blockavg', '', defDPF, defBL, defEpoch, defDrift, ...
    true, {'sci', 'cardiac'}, sciCardiacParams, defBio);

% --- QC-C: Full QC (all 4 checks) + TDDR + BPF ---
%     SCI + cardiac + CoV + Takizawa — most conservative rejection
fullQCParams = struct('SCIThreshold', 0.75, 'CardiacSNR', 3, ...
    'CoVThreshold', 0.1, 'TakizawaStrict', false);
pipeList{end+1} = makePipeline( ...
    'qc_full_tddr_bpf', ...
    'Full QC (SCI+cardiac+CoV+Takizawa) + TDDR + BPF', ...
    {{'pf2_Intensity2OD', {}}, {'pf2_MotionCorrectTDDR', {}}, {'bpf', {}}}, {}, ...
    false, '', 'blockavg', '', defDPF, defBL, defEpoch, defDrift, ...
    true, {'sci', 'cardiac', 'cov', 'takizawa'}, fullQCParams, defBio);

% --- QC-D: SCI + TDDR + SSR + GLM (OLS) ---
%     Common modern pipeline: QC rejection, motion correction, SSR, GLM
pipeList{end+1} = makePipeline( ...
    'qc_sci_tddr_ssr_glm', ...
    'SCI QC + TDDR + SSR + GLM (OLS)', ...
    {{'pf2_Intensity2OD', {}}, {'pf2_MotionCorrectTDDR', {}}}, {}, ...
    true, 'nearest', 'glm', 'OLS', defDPF, defBL, defEpoch, defDrift, ...
    true, {'sci'}, sciParams, defBio);

% --- QC-E: SCI + Spline + Wavelet (hybrid) ---
%     QC rejection + hybrid motion correction
pipeList{end+1} = makePipeline( ...
    'qc_sci_spline_wavelet', ...
    'SCI QC + spline-wavelet hybrid motion correction', ...
    {{'pf2_Intensity2OD', {}}, {'pf2_MotionCorrectSpline', {}}, {'pf2_MotionCorrectWavelet', {}}}, {}, ...
    false, '', 'blockavg', '', defDPF, defBL, defEpoch, defDrift, ...
    true, {'sci'}, sciParams, defBio);

% --- QC-F: Full QC + TDDR + SSR + GLM (AR-IRLS) — "kitchen sink" ---
%     Most complete pipeline: all QC checks, motion correction, SSR, AR-IRLS
pipeList{end+1} = makePipeline( ...
    'qc_full_tddr_ssr_glm_arirls', ...
    'Full QC + TDDR + SSR + GLM (AR-IRLS)', ...
    {{'pf2_Intensity2OD', {}}, {'pf2_MotionCorrectTDDR', {}}}, {}, ...
    true, 'nearest', 'glm', 'AR-IRLS', defDPF, defBL, defEpoch, defDrift, ...
    true, {'sci', 'cardiac', 'cov', 'takizawa'}, fullQCParams, defBio);

% =====================================================================
%  SECTION 3: Standardized cross-toolbox comparison pipeline
% =====================================================================
% TDDR + BPF + GLM OLS with canonical HRF, polynomial drift order 3,
% DPF=5.93 (Fixed mode). Matches standardized parameters across all
% 4 toolboxes for direct comparison. No SSR, no QC.
pipeList{end+1} = makePipeline( ...
    'standardized_glm', ...
    'Standardized cross-toolbox GLM: TDDR + BPF + OLS, canonical HRF, drift order 3', ...
    {{'pf2_Intensity2OD', {}}, {'pf2_MotionCorrectTDDR', {}}, {'bpf', {}}}, {}, ...
    false, '', 'glm', 'OLS', defDPF, defBL, defEpoch, defDrift, ...
    defQC, defQCChecks, defQCParams, defBio);

% Convert cell array to struct array
pipelines = [pipeList{:}];

end

%%_Subfunctions_________________________________________________________

function p = makePipeline(name, desc, rawM, oxyM, ssr, ssrM, analysis, ...
    glmM, dpf, blWin, epochWin, driftOrd, useQC, qcChecks, qcParams, biomarker)
    p.name = name;
    p.description = desc;
    p.rawMethods = rawM;
    p.oxyMethods = oxyM;
    p.useSSR = ssr;
    p.ssrMethod = ssrM;
    p.analysisType = analysis;
    p.glmMethod = glmM;
    p.dpfMode = dpf;
    p.baselineWindow = blWin;
    p.epochWindow = epochWin;
    p.glmDriftOrder = driftOrd;
    p.useQC = useQC;
    p.qcChecks = qcChecks;
    p.qcParams = qcParams;
    p.biomarker = biomarker;
end
